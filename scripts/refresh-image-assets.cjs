/**
 * Regenerate high-quality WebP derivatives from source photos.
 * Run after replacing originals in public/images/.
 * Usage: node scripts/refresh-image-assets.cjs
 */
const fs = require("fs");
const path = require("path");
const sharp = require("sharp");

const root = path.join(__dirname, "..");
const imagesDir = path.join(root, "public", "images");
const sizesOut = path.join(root, "lib", "image-sizes.ts");

const JPEG_SOURCES = [
  {
    file: "collageOutDoor-2.jpg",
    webpQuality: 92,
    jpegQuality: 93,
    sharpen: true,
  },
  {
    file: "collageOutDoor-1.jpg",
    webpQuality: 90,
    jpegQuality: 92,
    sharpen: false,
  },
];

async function refreshJpegSource({ file, webpQuality, jpegQuality, sharpen }) {
  const input = path.join(imagesDir, file);
  if (!fs.existsSync(input)) {
    console.warn("Skip missing:", file);
    return;
  }

  const base = file.replace(/\.jpe?g$/i, "");
  const webpOut = path.join(imagesDir, `${base}.webp`);
  const meta = await sharp(input).metadata();
  console.log(`${file}: ${meta.width}×${meta.height}`);

  let pipeline = sharp(input).rotate();
  if (sharpen) {
    pipeline = pipeline.sharpen({ sigma: 0.8, m1: 0.5, m2: 0.5 });
  }

  await pipeline
    .webp({ quality: webpQuality, effort: 4, smartSubsample: true })
    .toFile(webpOut);

  const webpMeta = await sharp(webpOut).metadata();
  const webpSize = fs.statSync(webpOut).size;
  console.log(
    `  → ${base}.webp ${webpMeta.width}×${webpMeta.height} (${Math.round(webpSize / 1024)}KB, q${webpQuality})`,
  );

  const jpegTmp = `${input}.refresh`;
  let jpegPipe = sharp(input).rotate();
  if (sharpen) jpegPipe = jpegPipe.sharpen({ sigma: 0.8, m1: 0.5, m2: 0.5 });
  await jpegPipe
    .jpeg({ quality: jpegQuality, mozjpeg: true, chromaSubsampling: "4:4:4" })
    .toFile(jpegTmp);
  fs.renameSync(jpegTmp, input);
  console.log(`  → refreshed ${file} (q${jpegQuality})`);
}

async function writeImageSizes() {
  const entries = {};
  const files = fs
    .readdirSync(imagesDir)
    .filter((f) => /\.(jpg|jpeg|webp)$/i.test(f));

  for (const file of files) {
    const meta = await sharp(path.join(imagesDir, file)).metadata();
    entries[`/images/${file}`] = {
      width: meta.width ?? 0,
      height: meta.height ?? 0,
    };
  }

  const lines = [
    "/** Auto-generated by scripts/refresh-image-assets.cjs — do not edit manually. */",
    "export type ImageSize = { width: number; height: number };",
    "",
    "const IMAGE_SIZES: Record<string, ImageSize> = {",
    ...Object.entries(entries)
      .sort(([a], [b]) => a.localeCompare(b))
      .map(([k, v]) => `  "${k}": { width: ${v.width}, height: ${v.height} },`),
    "};",
    "",
    "export function getImageSize(src: string): ImageSize | undefined {",
    "  return IMAGE_SIZES[src];",
    "}",
    "",
  ];
  fs.writeFileSync(sizesOut, lines.join("\n"));
  console.log("Wrote", sizesOut);
}

async function main() {
  for (const source of JPEG_SOURCES) {
    await refreshJpegSource(source);
  }
  await writeImageSizes();
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
