/**
 * Regenerate lib/image-sizes.ts from everything under public/images.
 */
const fs = require("fs");
const path = require("path");
const sharp = require("sharp");

const root = path.join(__dirname, "..");
const imagesDir = path.join(root, "public", "images");
const sizesOut = path.join(root, "lib", "image-sizes.ts");

async function main() {
  const entries = {};

  function walk(dir, webPrefix) {
    if (!fs.existsSync(dir)) return;
    for (const name of fs.readdirSync(dir)) {
      const full = path.join(dir, name);
      if (fs.statSync(full).isDirectory()) walk(full, `${webPrefix}/${name}`);
      else if (/\.(jpg|jpeg|png|webp|svg)$/i.test(name)) {
        entries[`${webPrefix}/${name}`.replace(/\\/g, "/")] = full;
      }
    }
  }

  walk(imagesDir, "/images");

  const sizeLines = [];
  for (const [rel, full] of Object.entries(entries).sort(([a], [b]) => a.localeCompare(b))) {
    if (rel.endsWith(".svg")) {
      sizeLines.push(`  "${rel}": { width: 0, height: 0 },`);
      continue;
    }
    const meta = await sharp(full).metadata();
    sizeLines.push(`  "${rel}": { width: ${meta.width ?? 0}, height: ${meta.height ?? 0} },`);
  }

  fs.writeFileSync(
    sizesOut,
    [
      "/** Auto-generated by scripts/regenerate-image-sizes.cjs — do not edit manually. */",
      "export type ImageSize = { width: number; height: number };",
      "",
      "const IMAGE_SIZES: Record<string, ImageSize> = {",
      ...sizeLines,
      "};",
      "",
      "export function getImageSize(src: string): ImageSize | undefined {",
      "  return IMAGE_SIZES[src];",
      "}",
      "",
    ].join("\n"),
  );

  console.log(`Wrote ${Object.keys(entries).length} entries to lib/image-sizes.ts`);
}

module.exports = { main };

if (require.main === module) {
  main().catch((e) => {
    console.error(e);
    process.exit(1);
  });
}
